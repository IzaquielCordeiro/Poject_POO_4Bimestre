package ClassesBD;

public class modificaLei {

	private int data;
	private int idLei;
	private int cpfDeputado;
	private int id;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getData() {
		return data;
	}

	public void setData(int data) {
		this.data = data;
	}

	public int getIdLei() {
		return idLei;
	}

	public void setIdLei(int idLei) {
		this.idLei = idLei;
	}

	public int getCpfDeputado() {
		return cpfDeputado;
	}

	public void setCpfDeputado(int cpfDeputado) {
		this.cpfDeputado = cpfDeputado;
	}
}