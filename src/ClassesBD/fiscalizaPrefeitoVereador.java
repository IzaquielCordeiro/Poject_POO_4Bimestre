package ClassesBD;

public class fiscalizaPrefeitoVereador {
	private int cpfPrefeito;
	private int data;
	private int cpfVereador;

	public int getCpfPrefeito() {
		return cpfPrefeito;
	}

	public void setCpfPrefeito(int cpfPrefeito) {
		this.cpfPrefeito = cpfPrefeito;
	}

	public int getData() {
		return data;
	}

	public void setData(int data) {
		this.data = data;
	}

	public int getCpfVereador() {
		return cpfVereador;
	}

	public void setCpfVereador(int cpfVereador) {
		this.cpfVereador = cpfVereador;
	}
}